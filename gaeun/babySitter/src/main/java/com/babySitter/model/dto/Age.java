package com.babySitter.model.dto;

public class Age{
	
	
	private String age_type;
	private int age_group_no;
	private int sitter_no;
	
	
	public String getAge_type() {
		return age_type;
	}
	public void setAge_type(String age_type) {
		this.age_type = age_type;
	}
	public int getAge_group_no() {
		return age_group_no;
	}
	public void setAge_group_no(int age_group_no) {
		this.age_group_no = age_group_no;
	}
	public int getSitter_no() {
		return sitter_no;
	}
	public void setSitter_no(int sitter_no) {
		this.sitter_no = sitter_no;
	}


	
}
